/*
Block comment
While not technically required in most cases, it is considered 
good form to end JavaScript statements with a semicolon (;). 
Code blocks, and their resulting scope, are defined with curly braces ({ }).
*/

function join(a, b) {
    return a + ' ' + b;
  }
  
  console.log(join('Hello', 'world'));
  // OUTPUT: Hello world